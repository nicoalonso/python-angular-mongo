import { existsSync, readFileSync, writeFileSync } from 'fs';
import dotenv from 'dotenv';

dotenv.config({ path: './.env.local' });
dotenv.config();

let template = '// This file is automatically generated by set-envs script\n';

if (existsSync('./VERSION')) {
  const version = readFileSync('./VERSION', { encoding: 'utf-8' });
  template += `export const APP_VERSION = '${version.trim()}';\n`;
} else {
  template += `export const APP_VERSION = 'beta';\n`;
}

const whiteList = [/^APP.*/, /^AAD.*/, /^API.*/];
for (const name in process.env) {
  if (whiteList.some((reg) => reg.test(name))) {
    template += `export const ${name} = '${process.env[name]}';\n`;
  }
}

const targetPath = './src/environments/envs.ts';
writeFileSync(targetPath, template);
